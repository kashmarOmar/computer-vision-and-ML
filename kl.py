class Queue:
    def __init__(self):
        self.array = []

    def check_length(self):
        if len(self.array) == 0:
            print("Empty queue")
        else:
            print(f"Queue length: {len(self.array)}")

    def insert(self, num):
        self.array.append(num)
        print(f"Element {num} added to the queue")

    def remove(self):
        if len(self.array) == 0:
            print("Empty queue, cannot remove element.")
        else:
            removed_element = self.array.pop(0)
            print(f"Removed element: {removed_element}")
            self.display()

    def display(self):
        if len(self.array) == 0:
            print("Empty queue")
        else:
            print("Queue:", self.array)

