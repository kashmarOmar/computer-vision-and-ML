import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt

# a) Load iris data and plot class visualization
iris = sns.load_dataset('iris')

# Plot class visualization (i) "sepal_length" vs "sepal_width"
plt.figure(figsize=(10, 6))
sns.scatterplot(x='sepal_length', y='sepal_width', hue='species', data=iris)
plt.title("Class Visualization: Sepal Length vs Sepal Width")
plt.show()

# Plot class visualization (ii) "sepal_length" vs "petal_width"
plt.figure(figsize=(10, 6))
sns.scatterplot(x='sepal_length', y='petal_width', hue='species', data=iris)
plt.title("Class Visualization: Sepal Length vs Petal Width")
plt.show()

# b) Compute the centroid of data for each species
centroid_data = iris.groupby('species').mean()

# Plot centroids for each class visualization
plt.figure(figsize=(10, 6))

sns.scatterplot(x='sepal_length', y='sepal_width', hue='species', data=iris)
plt.scatter(centroid_data['sepal_length'], centroid_data['sepal_width'], marker='X', s=200, c='black', label='Centroid')
plt.title("Class Visualization with Centroids: Sepal Length vs Sepal Width")
plt.legend()
plt.show()

plt.figure(figsize=(10, 6))
