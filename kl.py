import numpy as np

# Given NumPy arrays
a = np.array([4, 5, 6, 7, 8, 9])
b = np.array([10, 16, 17, 20, 18, 22])

# a) Calculate the Euclidean distance
euclidean_distance = np.linalg.norm(a - b)
print(f"a) Euclidean Distance: {euclidean_distance}")

# b) Calculate the generalised p-norm for p=3
p_norm = np.linalg.norm(a - b, ord=3)
print(f"b) Generalised p-norm (p=3): {p_norm}")

# c) Calculate the KL divergence DKL(a||b) and DKL(b||a)
kl_divergence_ab = np.sum(a * np.log(a / b))
kl_divergence_ba = np.sum(b * np.log(b / a))
# calculated directly based on the arrays a and b
print(f"c) KL Divergence DKL(a||b): {kl_divergence_ab}")
print(f"   KL Divergence DKL(b||a): {kl_divergence_ba}")

#Ensuring that arrays a and b are treated as probability distributions.
# Normalize arrays for KL divergence
